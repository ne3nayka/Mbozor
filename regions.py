def get_districts_for_region(region_name: str) -> List[str]:
    """Возвращает список районов (туманов) для указанного региона.

    Args:
        region_name (str): Название региона (например, "Андижон вилояти").

    Returns:
        List[str]: Список районов региона или пустой список, если регион не найден.
    """
    return REGIONS_DATA.get(region_name, [])